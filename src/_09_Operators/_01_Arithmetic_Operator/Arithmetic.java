// arithmetic operators are use to perform mathematical operation on to operands
// It is a binary operator as it performs operation on two operands

package _09_Operators._01_Arithmetic_Operator;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 4;
        System.out.println("Addition = " + (a + b)); // addition

        System.out.println("Substraction = " + (a - b)); // Substraction

        System.out.println("Multiplication = " + (a * b)); // MUltiplication

        System.out.println("Divide = " + (a / b)); // Division

        System.out.println("Remainder (modulo) = " + (a % b)); // Remainder
    }
}
