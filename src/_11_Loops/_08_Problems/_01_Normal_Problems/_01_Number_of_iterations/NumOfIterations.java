package _11_Loops._08_Problems._01_Normal_Problems._01_Number_of_iterations;

public class NumOfIterations {
    public static void main(String[] args) {
        int count = 0;
        int i = 0;
        while (i<=5){
            count++;
            i++;
        }
        System.out.println("Total number of iterations = " + count);
    }
}
