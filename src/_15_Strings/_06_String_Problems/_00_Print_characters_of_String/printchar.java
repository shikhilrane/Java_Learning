package _15_Strings._06_String_Problems._00_Print_characters_of_String;

public class printchar {
        public static void main(String[] args) {
            String name = "Shikhil";
            for (int i = 0; i < name.charAt(i); i++) {
                System.out.println(name.charAt(i));
            }
        }
}

// .charAt() method is use to extract character from the string